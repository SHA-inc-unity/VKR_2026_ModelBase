type MessageKey = keyof typeof messages.en;

function formatTemplate(template: string, params?: Record<string, string | number>): string {
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(params[key] ?? `{${key}}`));
}

export function getAnomalyText(locale: Locale) {
  return (key: MessageKey, params?: Record<string, string | number>) =>
    formatTemplate(messages[locale][key], params);
}

export function getAnomalyTypeLabel(locale: Locale, type: string): string {
  const dict = anomalyTypeLabels[locale] as Record<string, string>;
  return dict[type] ?? type;
}

export function getAnomalySuggestion(locale: Locale, type: string): string {
  const dict = suggestions[locale] as Record<string, string>;
  return dict[type] ?? getAnomalyTypeLabel(locale, type);
}

export function getAnomalySeverityLabel(locale: Locale, severity: 'critical' | 'warning' | 'info'): string {
  return severityLabels[locale][severity];
}

export function getCleanOpLabel(locale: Locale, op: string): string {
  const dict = cleanOpLabels[locale] as Record<string, string>;
  return dict[op] ?? op;
}

export function localizeAnomalyRuntimeMessage(locale: Locale, message: string): string {
  let localized = message;
  for (const [needle, replacement] of Object.entries(runtimeMessageMap[locale])) {
    localized = localized.replaceAll(needle, replacement);
  }
  return localized;
}

export function localizeAnomalyDetails(
  locale: Locale,
  anomalyType: string,
  details: string | null | undefined,
  column?: string | null,
): string | null {
  if (!details) return null;
  const t = getAnomalyText(locale);

  switch (anomalyType) {
    case 'gap':
      return t('detailMissingTimestamp');
    case 'duplicate': {
      const match = details.match(/^(\d+) rows share this timestamp$/);
      return match ? t('detailRowsShareTimestamp', { count: match[1] }) : details;
    }
    case 'negative_value':
      return column ? t('detailNegativeValue', { column }) : details;
    case 'zero_streak': {
      const match = details.match(/^zero\/null streak of (\d+) in (.+)$/);
      return match ? t('detailZeroStreak', { count: match[1], column: match[2] }) : details;
    }
    case 'iqr':
      return t('detailOutsideTukeyFence');
    case 'stale_price': {
      const match = details.match(/^frozen run length (\d+) \(≥ (\d+)\)$/);
      return match ? t('detailFrozenRun', { count: match[1], minLen: match[2] }) : details;
    }
    case 'dbscan':
      return t('detailMultivariateOutlier');
    case 'isolation_forest':
      return t('detailIsolationForestOutlier');
    default:
      return locale === 'ru'
        ? details
            .replace('threshold', 'порог')
            .replace('window', 'окно')
            .replace('outside Tukey fence', 'вне границы Tukey')
            .replace('deviation', 'отклонение')
            .replace('tolerance', 'допуск')
            .replace('prev=', 'предыдущее=')
        : details;
  }
}